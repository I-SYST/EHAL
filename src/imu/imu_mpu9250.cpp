int ImuMpu9250::Read(uint16_t Addr, uint8_t *pBuff, int Len)
{
	uint8_t regaddr;
	uint8_t d[2];
	int cnt = 0;

	while (Len > 0)
	{
		int l = min(Len, MPU9250_DMP_MEM_PAGE_SIZE);

		regaddr = MPU9250_DMP_MEM_BANKSEL;
		d[0] = Addr >> 8;
		d[1] = Addr & 0xFF;

		if (Write(&regaddr, 1, d, 2) < 2)
		{
			break;
		}

		regaddr = MPU9250_DMP_MEM_RW;
		l = Read(&regaddr, 1, pBuff, l);

		pBuff += l;
		Addr += l;
		Len -= l;
		cnt += l;
	}

	return cnt;
}

